export function formatListingDate(submittedOn: string): string {
  const [mo, d, y] = submittedOn.split('/').map((x) => parseInt(x, 10))
  if (!mo || !d || !y) return submittedOn
  const dt = new Date(y, mo - 1, d)
  return dt.toLocaleDateString('en-US', { month: 'short', day: 'numeric', year: 'numeric' })
}

